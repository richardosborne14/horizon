@pytest.mark.asyncio
async def test_list_returns_only_published(client):
    """
    GET /api/blog/ must return at least the published article and NOT
    include the unpublished one.
    """
    pub_slug = f"pub-list-{_RUN_ID}"
    draft_slug = f"draft-list-{_RUN_ID}"
    try:
        await _create_article(slug=pub_slug, title="Article publié", is_published=True)
        await _create_article(slug=draft_slug, title="Brouillon liste", is_published=False)

        resp = await client.get("/api/blog/")
        assert resp.status_code == 200

        slugs = [a["slug"] for a in resp.json()]
        assert pub_slug in slugs
        assert draft_slug not in slugs
    finally:
        await _cleanup_articles([pub_slug, draft_slug])


