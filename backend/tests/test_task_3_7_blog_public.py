@pytest.mark.asyncio
async def test_list_ordered_newest_first(client):
    """
    Results should be sorted by published_at DESC (newest first).
    We create two articles with known timestamps and verify ordering.
    """
    slug_old = f"old-{_RUN_ID}"
    slug_new = f"new-{_RUN_ID}"
    try:
        await _create_article(
            slug=slug_old,
            title="Vieil article",
            published_at=datetime(2024, 1, 1, tzinfo=timezone.utc),
        )
        await _create_article(
            slug=slug_new,
            title="Nouvel article",
            published_at=datetime(2025, 6, 1, tzinfo=timezone.utc),
        )

        resp = await client.get("/api/blog/")
        assert resp.status_code == 200

        articles = resp.json()
        slugs = [a["slug"] for a in articles]

        # Both test articles must be present
        assert slug_new in slugs
        assert slug_old in slugs

        # Newer must come BEFORE older
        assert slugs.index(slug_new) < slugs.index(slug_old)
    finally:
        await _cleanup_articles([slug_old, slug_new])


