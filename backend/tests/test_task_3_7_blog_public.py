# ── Unit tests: _resolve_body helper ─────────────────────────────────────────


def _make_article(
    *,
    content: str | None = "<p>original</p>",
    body_html_cleaned: str | None = None,
    published_version: str = "original",
) -> SimpleNamespace:
    """
    Build a lightweight object for unit-testing _resolve_body.

    WHY SimpleNamespace not BlogArticle: BlogArticle.__new__() bypasses
    SQLAlchemy's mapper instrumentation, causing AttributeError when setting
    attributes. SimpleNamespace is sufficient because _resolve_body only reads
    .content, .body_html_cleaned, .published_version.

    Args:
        content:            Original HTML.
        body_html_cleaned:  AI-cleaned HTML (optional).
        published_version:  "original" or "cleaned".

    Returns:
        SimpleNamespace with the three attributes _resolve_body reads.
    """
    return SimpleNamespace(
        content=content,
        body_html_cleaned=body_html_cleaned,
        published_version=published_version,
    )


def test_resolve_body_uses_cleaned_version():
    """
    _resolve_body must return body_html_cleaned when published_version == 'cleaned'
    and body_html_cleaned is not None/empty.
    """
    article = _make_article(
        content="<p>original</p>",
        body_html_cleaned="<p>cleaned</p>",
        published_version="cleaned",
    )
    assert _resolve_body(article) == "<p>cleaned</p>"


def test_resolve_body_uses_original_version():
    """
    _resolve_body must return content when published_version == 'original'.
    """
    article = _make_article(
        content="<p>original</p>",
        body_html_cleaned="<p>cleaned</p>",
        published_version="original",
    )
    assert _resolve_body(article) == "<p>original</p>"


def test_resolve_body_cleaned_missing_falls_back():
    """
    _resolve_body must fall back to content when published_version == 'cleaned'
    but body_html_cleaned is None (e.g. enhancement failed).
    """
    article = _make_article(
        content="<p>original fallback</p>",
        body_html_cleaned=None,
        published_version="cleaned",
    )
    assert _resolve_body(article) == "<p>original fallback</p>"


def test_resolve_body_empty_content_returns_empty_string():
    """
    _resolve_body must return '' (not None) when content is None.
    """
    article = _make_article(
        content=None,   # type: ignore[arg-type]
        body_html_cleaned=None,
        published_version="original",
    )
    assert _resolve_body(article) == ""
