# ── Helper ────────────────────────────────────────────────────────────────────


async def _create_article(
    *,
    slug: str,
    title: str,
    content: str = "<p>Corps original.</p>",
    body_html_cleaned: str | None = None,
    published_version: str = "original",
    is_published: bool = True,
    published_at: datetime | None = None,
) -> SimpleNamespace:
    """
    Insert a BlogArticle via raw SQL, bypassing the ORM.

    WHY raw SQL: the BlogArticle model has a pgvector `embedding` column.
    SQLAlchemy cannot send None for a vector column via the asyncpg dialect
    (DatatypeMismatchError). Raw SQL lets us omit the column entirely so
    the DB default (NULL) applies.

    Args:
        slug:              URL-safe slug (embed a uuid fragment for uniqueness).
        title:             Article title.
        content:           Original HTML body.
        body_html_cleaned: AI-cleaned version (optional).
        published_version: "original" or "cleaned".
        is_published:      Whether the article is visible publicly.
        published_at:      Publication datetime (defaults to now).

    Returns:
        SimpleNamespace with .slug, .title, .is_published attributes.
    """
    effective_at = published_at or datetime.now(timezone.utc)
    async with AsyncSessionLocal() as db:
        await db.execute(
            text("""
                INSERT INTO blog_articles
                    (id, slug, title, content, body_html_cleaned,
                     published_version, is_published, published_at, excerpt)
                VALUES
                    (:id, :slug, :title, :content, :body_html_cleaned,
                     :published_version, :is_published, :published_at, :excerpt)
            """),
            {
                "id": str(uuid.uuid4()),
                "slug": slug,
                "title": title,
                "content": content,
                "body_html_cleaned": body_html_cleaned,
                "published_version": published_version,
                "is_published": is_published,
                "published_at": effective_at,
                "excerpt": f"Extrait de {title}",
            },
        )
        await db.commit()

    return SimpleNamespace(slug=slug, title=title, is_published=is_published)


async def _cleanup_articles(slugs: list[str]) -> None:
    """Remove test articles by slug to keep the DB clean."""
    if not slugs:
        return
    # Use string formatting — safe because slugs are generated by test code.
    placeholders = ", ".join(f"'{s}'" for s in slugs)
    async with AsyncSessionLocal() as db:
        await db.execute(text(f"DELETE FROM blog_articles WHERE slug IN ({placeholders})"))
        await db.commit()


